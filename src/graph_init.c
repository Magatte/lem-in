#include "lemin.h"

t_dlist					*graph_init(t_dlist *rooms)
{
	t_room				*tmp;

	tmp = rooms->head;
	while (tmp)
	{
		tmp->visited = 0;
		tmp = tmp->next;
	}
	return (rooms);
}
